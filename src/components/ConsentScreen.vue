<template>
  <div class="col-md-12">
    <div class="card card-container">
      <img
          id="profile-img"
          src="//ssl.gstatic.com/accounts/ui/avatar_2x.png"
          class="profile-img-card"
      />
      <Form @submit="handleLogin" :validation-schema="schema">
        <div class="form-group">
          <label>Image</label>
        </div>
        <div class="form-group">
          <strong><label >You may be sharing sensitive info with this site or app.</label></strong>
        </div>

        <div class="form-group">
          <button class="btn btn-primary btn-block" :disabled="loading">
            <span
                v-show="loading"
                class="spinner-border spinner-border-sm"
            ></span>
            <span>Allow</span>
          </button>
        </div>
      </Form>
    </div>
  </div>
</template>

<script>
export default {
  name: "ConsentScreen",
  methods: {
    handleLogin(user) {
      this.loading = true;
      this.$store.dispatch("auth2/allow", user)
    },
  },
  computed: {
    allow() {
      return this.$store.state.auth2.status.allow;
    },
  },
  mounted() {
    if (this.allow) {
      window.location.href = 'http://localhost:8084/image';
    }
  },
};
</script>
